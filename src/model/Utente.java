package model;

public class Utente {
  String nome;
  String cognome;
  String email;
  String userId;
  String password;

  public Utente(String nome, String cognome, String email, String userId, String password) {
    this.nome = nome;
    this.cognome = cognome;
    this.email = email;
    this.userId = userId;
    this.password = password;
  }


}
